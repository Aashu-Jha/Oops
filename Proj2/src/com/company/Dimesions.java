package com.company;

public class Dimesions {
    private int height;
    private int width;
    private  int length;

    public Dimesions(int height, int width, int length) {
        this.height = height;
        this.width = width;
        this.length = length;
    }

    public int getHeight() {
        return height;
    }

    public int getWidth() {
        return width;
    }

    public int getlength() {
        return length;
    }
}
